	<h1>what is It</h1><br>
	<h3>Regional Opportunity Initiatives funds and assists cohorts of school districts through a 10-month design thinking process to better align PK-12 curricular and programmatic offerings with the education and workforce needs of their communities. This initiative has a particular focus on the Indiana Uplandsâ€™ key industry sectors: advanced manufacturing, life sciences, and national security and defense.

Core Principles:
Every student is engaged in a relevant path to success.
Students graduate high school ready for post-secondary and career success.
Meaningful and ongoing collaboration occurs among schools, industry, and community.
Teaching and learning are grounded in relevancy.
PK-12 schools are aligned around a common vision of student success.
Schools embrace the significant role they play in achieving regional prosperity.
ROI developed the Ready Schools Initiative based largely upon the design thinking framework of the global design company IDEO and the coalition-building processes from CivicLab. School districts within the 11-county region of the Indiana Uplands served by ROI are welcome to apply. If selected, districts receive funding and coaching through a 10-month development process and may be eligible to pursue implementation funding through ROI at the end of the development grant period.

Cohort Selection:
Applying districts are required to complete the online readiness assessment to help school districts and communities understand their current PK-12 system and its level of alignment to education and workforce needs. Findings are used to identify priority areas for coaching and support.

Two Ready Schools development grants were awarded for the 2017-18 school year. Four development grants were awarded in the second cohort for the 2018-19 school year. Three development grants were awarded in the third cohort for the 2019-20 school year. Cohorts were able to learn from and with one another throughout the development process.

Timeframe:
Selected cohort districts will have 10 months to proceed through the first four phases of the Ready Schools Design Process to be ready to apply for implementation funding.</h3>


